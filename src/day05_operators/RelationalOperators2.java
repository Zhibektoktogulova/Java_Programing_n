package day05_operators;

import jdk.swing.interop.SwingInterOpUtils;

public class RelationalOperators2 {

    public static void main(String[] args) {

        // Equal  ==;

        System.out.println(1000 == 2000);

        System.out.println("Java" == "Java");

        System.out.println("java" == "JAVA");

        System.out.println("Muhtar" == "Good guy"); //

        System.out.println(true == false);

        // Not equal !=

        System.out.println(1000 != 2000); // it's not equal and gives me true

        System.out.println("Java" != "Java"); //it's equal it gives me false

        System.out.println(1000 != 2000);

        System.out.println("Java" != "Java");

        System.out.println("java" != "JAVA");

        System.out.println("Muhtar" != "Good guy"); //

        System.out.println(true != false);









    }
}
