package day05_operators;

public class ShorthandOperators {

    public static void main(String[] args) {

        int a = 20;

        System.out.println(a); // 20

        System.out.println(a); // 20

        a = 40;

        System.out.println(a); // 40

        a = 90;

        System.out.println(a); //90

        System.out.println("----------------------------------");












    }
}
